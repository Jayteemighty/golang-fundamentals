// Data Types
package main
import "fmt"

func main(){
	// Integers- int, int8, int16, int32, int64
	var num int = 42

	// Floats- float32, float64
	var pi float32 = 3.14

	// Strings
	var name string = "Adesina"

	// Booleans (true or false)
	var isGrown bool = true

	// Declaring constants (const)
	const Pi = 3.14
	const greetings = "Fuck you!"


	fmt.Println(num)
	fmt.Println(pi)
	fmt.Println(name)
	fmt.Println(isGrown)
	fmt.Println(Pi, greetings)
}